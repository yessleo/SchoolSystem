const STATUS_OPTIONS = [{ value: 'all', label: 'All' }, ...USER_STATUS_OPTIONS];

const TABLE_HEAD = [
  { id: 'username', label: 'Username', width: 180 },
  { id: 'role', label: 'Role', width: 180 },
  { id: 'status', label: 'Status', width: 180 },
  { id: 'created_at', label: 'Created at', width: 180 },
  { id: '', width: 88 },
];

